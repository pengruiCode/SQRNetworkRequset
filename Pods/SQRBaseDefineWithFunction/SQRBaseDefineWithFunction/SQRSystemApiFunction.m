NSString *const KConnectStateNoti = @"ReachabilityChangeBySystemApiUtilNoti";
@interface SQRSystemApiFunction()
@property (nonatomic,strong) Reachability* reach;
@end
@implementation SQRSystemApiFunction
static Reachability* _reach;
static SQRSystemApiFunction *_instance;
+ (instancetype)sharedInstance {
    
    static dispatch_once_t onceToken;
    dispatch_once(&onceToken, ^{
        _instance = [[self alloc] init];
    });
    return _instance;
}
+ (void)getConnectionModeRealTime {
    [[NSNotificationCenter defaultCenter] addObserver:[self sharedInstance]
     
                                             selector:@selector(reachabilityChanged:)
     
                                                 name:kReachabilityChangedNotification object:nil];
    
    // 获取访问指定站点的Reachability对象
    
    _reach = [Reachability reachabilityWithHostName:@"www.baidu.com"];
    
    // 让Reachability对象开启被监听状态
    
    [_reach startNotifier];
}

- (void)reachabilityChanged:(NSNotification *)noti {
    
    // 通过通知对象获取被监听的Reachability对象
    
    Reachability *curReach = [noti object];
    
    // 获取Reachability对象的网络状态
    
    NetworkStatus status = [curReach currentReachabilityStatus];
    
    if (status == NotReachable) {
        [[NSNotificationCenter defaultCenter] postNotificationName:KConnectStateNoti object:@{@"state":@"1"}];
    } else if (status == ReachableViaWiFi) {
        [[NSNotificationCenter defaultCenter] postNotificationName:KConnectStateNoti object:@{@"state":@"2"}];
    } else {
        [[NSNotificationCenter defaultCenter] postNotificationName:KConnectStateNoti object:@{@"state":@"3"}];
    }
}

+ (NSInteger)getConnectionMode {
    NSInteger isExistenceNetwork = 0;
    Reachability *reach = [Reachability reachabilityWithHostName:@"www.baidu.com"];
    switch ([reach currentReachabilityStatus]) {
        case NotReachable:
            // 无网络
            isExistenceNetwork = 0;
            
            break;
        case ReachableViaWiFi:
            // wifi
            isExistenceNetwork = 1;
            
            break;
        case ReachableViaWWAN:
            // 3G
            isExistenceNetwork = 2;
            
            break;
    }
    
    return isExistenceNetwork;
}

